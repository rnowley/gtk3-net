namespace Gtk3
{
    /// <summary>
    /// A <see cref="Window"/> can be one of these types.
    /// </summary>
    public enum WindowType
    {
        /// <summary>
        /// A regular window, such as a dialog.
        /// </summary>
        TopLevelWindow,
        /// <summary>
        /// A special window such as a tooltip.
        /// </summary>
        PopupWindow
    }
}