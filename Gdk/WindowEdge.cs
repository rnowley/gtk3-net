namespace Gtk3.Gdk
{
    /// <summary>
    /// Determines a window edge or corner.
    /// </summary>
    public enum WindowEdge
    {
        /// <summary>
        /// the top left corner.
        /// </summary>
        NorthWest,
        /// <summary>
        /// the top edge.
        /// </summary>
        North,
        /// <summary>
        /// the top right corner.
        /// </summary>
        NorthEast,
        /// <summary>
        /// the left edge.
        /// </summary>
        West,
        /// <summary>
        /// the right edge.
        /// </summary>
        East,
        /// <summary>
        /// the lower left corner.
        /// </summary>
        SouthWest,
        /// <summary>
        /// the lower edge.
        /// </summary>
        South,
        /// <summary>
        /// the lower right corner.
        /// </summary>
        SouthEast
    }
}
