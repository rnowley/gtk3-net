namespace Gtk3
{
    /// <summary>
    /// Describes how LevelBar contents should be rendered. Note that this enumeration
    /// could be extended with additional modes in the future.
    /// </summary>
    public enum LevelBarMode
    {
        /// <summary>
        /// the bar has a continuous mode
        /// </summary>
        Continuous,
        /// <summary>
        /// the bar has a discrete mode
        /// </summary>
        Discrete
    }
}